int SoftTimer(SoftTimerData *TIM, unsigned short __TIMEVALUE)
{
	char _flag = 0;
	TIM->currentSTT = __flagSysTick;

	if(TIM->lastSTT != TIM->currentSTT)
	{
		TIM->lastSTT = TIM->currentSTT;

		if((TIM->cnt++) >= __TIMEVALUE)
		{
			TIM->cnt = 0;
			 _flag = 1;
		}
	}

	return _flag;
}
